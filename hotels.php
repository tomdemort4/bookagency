<?php

/*
------------------------------------------------------------------------------------------------
BOOT
------------------------------------------------------------------------------------------------

*/

include 'resources/Request.php';
include 'repositories/HotelRepository.php';

/*
------------------------------------------------------------------------------------------------
CLASS INSTANCES 
------------------------------------------------------------------------------------------------

*/

$request = new Request();
$hotelRepository = new HotelRepository();

/*
------------------------------------------------------------------------------------------------
API
------------------------------------------------------------------------------------------------

*/


if ($request->isGet()) {
	if(isset($_GET['id'])){
		$id = intval($_GET['id']);
		$result = $hotelRepository->getById($id);
		echo json_encode($result);
	}else{
		$result = $hotelRepository->all();
		echo json_encode($result);
	}
}
